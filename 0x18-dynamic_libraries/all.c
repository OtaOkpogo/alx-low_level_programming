#include <stdio.h>


/**
 *  * add - This  will add two integers together
 *   * @a: this is the first unit
 *    * @b: this is the second unit
 *     * Return: the addition of the integers
 *      */
int add(int a, int b)
{
	        return (a + b);
}


/**
 *  * sub - this will subtract two integers together
 *   * @a: this is the first integer
 *    * @b: this is the second integer
 *     * Return: the result of the subtraction of the integers
 *      */
int sub(int a, int b)
{
	        return (a - b);
}


/**
 *  * mul - this will multiply two integers together
 *   * @a: this is the first unit
 *    * @b: this is the second unit
 *     * Return: the multiplication of the integers
 *      */
int mul(int a, int b)
{
	        return (a * b);
}
/**
 *  * div - This is to divide two integers together
 *   * @a: this is the first unit
 *    * @b: this is the second unit
 *     * Return: the division of the integers
 *      */
int div(int a, int b)
{
	        return (a / b);
}

/**
 *  * mod - This is the mod of two integers together
 *   * @a: this is the first unit
 *    * @b: this is the second unit
 *     * Return: the answer
 *      */
int mod(int a, int b)
{
	        return (a % b);
}
